def find_weather_presets():
    rgx = re.compile('.+?(?:(?<=[a-z])(?=[A-Z])|(?<=[A-Z])(?=[A-Z][a-z])|$)')
    name = lambda x: ' '.join(m.group(0) for m in rgx.finditer(x))
    presets = [x for x in dir(carla.WeatherParameters) if re.match('[A-Z].+', x)]
    return [(getattr(carla.WeatherParameters, x), name(x)) for x in presets]


def get_actor_display_name(actor, truncate=250):
    name = ' '.join(actor.type_id.replace('_', '.').title().split('.')[1:])
    return (name[:truncate - 1] + u'\u2026') if len(name) > truncate else name

def preprocess_image(image):
    input_size = (416, 416)
    image_resize = cv2.resize(image, input_size)
    image_resize = image_resize / 255.0
    image_tensor = torch.from_numpy(image_resize).permute(2, 0, 1).unsqueeze(0).float()

    device = torch.device('cuda') # change to gpu
    image_tensor = image_tensor.to(device)
    return image_tensor

def detect_traffic_light(image_tensor):
    global model
    with torch.no_grad():
        predictions = model(image_tensor, augment=False)
        predictions = non_max_suppression(predictions, conf_thres=0.5, iou_thres=0.45)
    torch.cuda.empty_cache()
    return predictions

def draw_bounding_box(predictions, image_processed):

    annotator = Annotator(image_processed, line_width=3, example=False)

    if len(predictions):
        # Process predictions
        for i, det in enumerate(predictions):

            if len(det):
                boxes = det[:, :4]
                scores = det[:, 4]
                classes = det[:, 5]

                for j in range(len(boxes)):
                    cls = int(classes[j].item())
                    if cls == 0:
                        x1, y1, x2, y2 = boxes[j]
                        x1 = x1 / 416 * IMAGE_WIDTH
                        x2 = x2 / 416 * IMAGE_WIDTH
                        y1 = y1 / 416 * IMAGE_HEIGHT
                        y2 = y2 / 416 * IMAGE_HEIGHT
                        conf = scores[j]

                        if int(x1) != int(x2) and int(y1) != int(y2) and int(x1) > 0 and int(x2) > 0 and int(y1) > 0 and int(y2) > 0 and (int(x2) - int(x1)) * (int(y2) - int(y1)) > 17 * 25:

                            image_cropped = image_processed[int(y1):int(y2)+1, int(x1):int(x2)+1, ::-1]

                            time_stamp = (round(time.time()*1000))
                            # print(f'Image name: {time_stamp}')

                            # cv2.imwrite("out/test/%d.png" % time_stamp, image_cropped)

                            traffic_light_state = detect_traffic_light_state(image_cropped)

                            colors = (0, 0, 0)
                            txt_colors = (0, 0, 0)
                            if traffic_light_state == 0:
                                state = 'Off'
                                colors = (0, 0, 0)
                                txt_colors = (255, 255, 255)
                            elif traffic_light_state == 1:
                                state = 'Red'
                                colors = (0, 0, 255)
                                txt_colors = (255, 255, 255)
                            elif traffic_light_state == 2:
                                state = 'Yellow'
                                colors = (0, 255, 255)
                            else: 
                                state = 'Green'
                                colors = (0, 255, 0)

                            annotator.box_label((x1, y1, x2, y2), label=f'{state}: {conf:.2f}', color=colors, txt_color=txt_colors)

    annotated_image = annotator.result()
    return annotated_image

def create_camera(player, world):
    blueprint_library = world.get_blueprint_library()
    car_blueprints = [bp for bp in blueprint_library.filter('vehicle') if bp.has_attribute('number_of_wheels') and bp.get_attribute('number_of_wheels').as_int() == 4]
    car_blueprints = [bp for bp in car_blueprints if not bp.has_attribute('is_motorcycle') and not bp.has_attribute('is_bicycle')]
    basic_cars = ['vehicle.carlamotors.carlacola', 'vehicle.lincoln.mkz2017', 'vehicle.nissan.patrol', 'vehicle.audi.tt', 'vehicle.tesla.cybertruck', 'vehicle.toyota.prius', 'vehicle.diamondback.century']
    car_blueprints = [bp for bp in car_blueprints if bp.id in basic_cars]
    camera_bp = blueprint_library.find('sensor.camera.rgb')
    # camera_bp.set_attribute('sensor_tick', '0.03')
    camera_bp.set_attribute('image_size_x', str(IMAGE_WIDTH))
    camera_bp.set_attribute('image_size_y', str(IMAGE_HEIGHT))
    camera_bp.set_attribute('fov', str(FOV))
    camera_transform = carla.Transform(carla.Location(x=1.0, z=2.0))
    camera = world.try_spawn_actor(camera_bp, camera_transform, attach_to=player)
    return camera

def calibrate_camera(camera):
    camera.blur_amount = 0.0
    camera.motion_blur_intensity = 0
    camera.motion_max_distortion = 0
    calibration = np.identity(3)
    calibration[0, 2] = IMAGE_WIDTH / 2.0
    calibration[1, 2] = IMAGE_HEIGHT / 2.0
    calibration[0, 0] = calibration[1, 1] = IMAGE_WIDTH / (2.0 * np.tan(FOV * np.pi / 360.0))
    camera.calibration = calibration
    return camera

def camera_callback(image):
    global is_image_set, capture, model
    if not is_image_set:
        image_processed = np.array(image.raw_data)
        image_processed = image_processed.reshape((IMAGE_HEIGHT, IMAGE_WIDTH, 4))
        image_processed = image_processed[:, :, :3]  # taking out opacity channel
        image_processed = image_processed.astype(np.uint8)
        # cv2.imshow('display', capture)
        # cv2.imwrite("out/test/%d.png" % (round(time.time()*1000)), capture)

        image_tensor = preprocess_image(image_processed)
        predictions = detect_traffic_light(image_tensor)
        image_with_boxes = draw_bounding_box(predictions, image_processed)
        capture = image_with_boxes
        is_image_set = True


