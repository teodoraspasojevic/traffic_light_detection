def game_loop(args):
    global is_image_set, capture, model
    pygame.init()
    pygame.font.init()
    world = None

    torch.cuda.empty_cache()

    weights_path = '/home/rtrk/teodora/traffic_light_detection/runs_cw/train_shuffle_ft/weights/best.pt'
    model = attempt_load(weights_path, device='cuda')
    model.eval()

    try:
        client = carla.Client(args.host, args.port)
        client.set_timeout(200.0)

        display = pygame.display.set_mode(
            (args.width, args.height),
            pygame.HWSURFACE | pygame.DOUBLEBUF)

        hud = HUD(args.width, args.height)
        world = World(client.load_world('Town05'), hud, args)
        controller = KeyboardControl(world, args.autopilot)

        camera = create_camera(world.player, world.world)
        if camera:
            calibrate_camera(camera)
            camera.listen(lambda image: camera_callback(image))

        cv2.namedWindow('capture', cv2.WINDOW_NORMAL)
        cv2.resizeWindow('capture', IMAGE_WIDTH, IMAGE_HEIGHT)

        clock = pygame.time.Clock()
        while True:
            is_image_set = False

            clock.tick_busy_loop(20)
            if controller.parse_events(client, world, clock):
                return
            world.tick(clock)
            world.render(display)
            pygame.display.flip()

            if is_image_set:
                cv2.imshow('capture', capture)
                cv2.waitKey(1)

    finally:

        if (world and world.recording_enabled):
            client.stop_recorder()

        if world is not None:
            world.destroy()

        if camera:
            camera.stop()
            camera.destroy()

        pygame.quit()

        cv2.destroyAllWindows()


